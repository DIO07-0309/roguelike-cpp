// ---- 查询接口 ----
const NPCData* get_npc_config(int floor, int slot) {
    struct Entry { int floor; int slot; const NPCData* data; };
    static const Entry MAP[] = {
        {2,0,&NPC_F2}, {3,0,&NPC_F3}, {4,0,&NPC_F4},
        {6,0,&NPC_F6}, {7,0,&NPC_F7}, {8,0,&NPC_F8},
        {9,0,&NPC_F9}, {11,0,&NPC_F11}, {12,0,&NPC_F12},
        {14,0,&NPC_F14},
    };
    for (auto& e : MAP)
        if (e.floor == floor && e.slot == slot) return e.data;
    return nullptr;
}

int get_npc_count_for_floor(int floor) {
    for (auto& e : {std::make_pair(2,1),{3,1},{4,1},{6,1},{7,1},{8,1},{9,1},{11,1},{12,1},{14,1}})
        if (e.first == floor) return e.second;
    return 0;
}
